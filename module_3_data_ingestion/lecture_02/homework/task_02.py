import math

# Запросить у пользователя идентификатор зоны хранения.
zone_id = input("Введите идентификатор зоны: ")

# Общая площадь квадратной зоны хранения в квадратных метрах
area_total = 225

# Длина одной стороны квадратной зоны.
side_length = math.sqrt(area_total)

print("Расчет для объекта:", zone_id)
print("Длина стены зоны:", side_length, "метров")



